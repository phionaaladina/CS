# Create a program to calculate the area of a triangle (1/2 * base * height). 
# Base and height should be entered using the keyboard. 


base = float(input('Enter the triangle base: ' ))
height = float(input('Enter the triange height: ' ))
area = 1/2 *base*height

print(f'The area of the triangle with a base {base} cm and height {height} cm is {area} cm')